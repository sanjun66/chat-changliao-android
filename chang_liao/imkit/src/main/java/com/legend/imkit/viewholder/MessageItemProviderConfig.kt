package com.legend.imkit.viewholder

class MessageItemProviderConfig {
    /** 是否显示头像。  */
    var showPortrait = true

    /** 是否横向居中显示。  */
    var centerInHorizontal = false

    /** 是否显示未发送成功警告。  */
    var showWarning = true

    /** 是否显示发送进度。  */
    var showProgress = true

    /** 会话界面是否在消息上面显示昵称。  */
    var showSummaryWithName = true

    /** 单聊会话中是否在消息旁边显示已读回执状态。 默认不显示  */
    var showReadState = false

    /** 是否需要展示气泡  */
    var showContentBubble = true
}